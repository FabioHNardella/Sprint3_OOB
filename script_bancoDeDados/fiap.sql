CREATE TABLE novo_colaborador(
    id_novo_colaborador NUMBER(5) PRIMARY KEY,
    nome_colaborador VARCHAR2(25),
    sobrenome_colaborador VARCHAR2(25),
    email VARCHAR2(25),
    telefone VARCHAR2(25)
);

CREATE TABLE buddy(
    id_novo_colaborador NUMBER(5) PRIMARY KEY,
    nome_colaborador VARCHAR2(25),
    sobrenome_colaborador VARCHAR2(25),
    email VARCHAR2(25),
    telefone VARCHAR2(25)
);

CREATE TABLE gestor(
    id_novo_colaborador NUMBER(5) PRIMARY KEY,
    nome_colaborador VARCHAR2(25),
    sobrenome_colaborador VARCHAR2(25),
    email VARCHAR2(25),
    telefone VARCHAR2(25)
);


